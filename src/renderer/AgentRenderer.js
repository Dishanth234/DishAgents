// 16x16 sprite frames per state (each row = one row of pixels, 16 cols)
// Encoded as a flat 256-element array, values are palette indices
function makeSprite(rows) {
  // rows is array of 16 strings, each 16 chars long, digit = palette index
  return rows.join('').split('').map(Number);
}

