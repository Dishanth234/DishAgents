export class AgentRenderer {
  constructor(canvas, animationController, config) {
    this.canvas = canvas;
    this.ctx    = canvas.getContext('2d');
    this.anim   = animationController;
    this.config = config;

    const variant = config.spriteConfig?.colorVariant ?? 'salmon';
    this.palette = COLOR_VARIANTS[variant] ?? COLOR_VARIANTS.salmon;

    // Direction facing: 1 = right, -1 = left
    this.facing = 1;
  }

  setFacing(dx) {
    if (Math.abs(dx) > 0.5) this.facing = dx > 0 ? 1 : -1;
  }

  draw(state, name) {
    const ctx = this.ctx;
    ctx.clearRect(0, 0, 80, 120);

    // Shadow
    ctx.save();
    ctx.globalAlpha = 0.18;
    ctx.fillStyle   = '#000';
    ctx.beginPath();
    ctx.ellipse(40, 90, 18, 4, 0, 0, Math.PI * 2);
    ctx.fill();
    ctx.restore();

    // Sprite
    const frames    = SPRITE_FRAMES[state] ?? SPRITE_FRAMES.idle;
    const frameIdx  = this.anim.currentFrame % frames.length;
    const frameData = frames[frameIdx];
    this._drawSprite(frameData);

    // Name label
    ctx.font        = 'bold 8px "Courier New", monospace';
    ctx.textAlign   = 'center';
    ctx.lineWidth   = 3;
    ctx.strokeStyle = 'rgba(0,0,0,0.75)';
    ctx.strokeText(name, 40, 113);
    ctx.fillStyle   = '#ffffff';
    ctx.fillText(name, 40, 113);
  }

  _drawSprite(frameData) {
    const ctx   = this.ctx;
    const S     = 3; // 3px per pixel-art pixel
    const SIZE  = 16;
    const p     = this.palette;

    const PALETTE = [
      null,       // 0 = transparent
      p.body,     // 1
      p.outline,  // 2
      p.eye,      // 3
      p.blush,    // 4
      p.leg,      // 5
      p.hi,       // 6
    ];

    // Center horizontally in 80px canvas, leave 8px top
    const baseX = 8;
    const baseY = 8;

    for (let row = 0; row < SIZE; row++) {
      for (let col = 0; col < SIZE; col++) {
        const ci = frameData[row * SIZE + col];
        if (ci === 0) continue;
        const color = PALETTE[ci];
        if (!color) continue;

        ctx.fillStyle = color;

        // Flip horizontally based on facing direction
        let drawCol = col;
        if (this.facing === -1) drawCol = SIZE - 1 - col;

        ctx.fillRect(baseX + drawCol * S, baseY + row * S, S, S);
      }
    }
  }
}
